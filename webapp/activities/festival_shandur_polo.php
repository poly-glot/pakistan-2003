    <p><b>Background</b></p>

    <p>Polo is an equestrian sport with its origin embedded in Central Asia
        dating back to 6th century BC. At first it was a training game for
        cavalry units for the King's guards or other elite troops. To the war
        like tribesmen who played polo with as many as 100 players to a side, it
        was a miniature battle. It became a Persian national game in the 6th
        century AD. From Persia, the game spread to Arabia, then to Tibet, China
        and Japan. In China, in the year 910, death of a favorite relative in a
        game prompted Emperor Apao-Chi to order beheading of all players!

    </p>
    <p>Polo was introduced in South Asia, by the Muslim conquerors in the 13th
        century. English word 'Polo" is a Balti word meaning, 'ball'. Now a
        days, there are six players to a side, but this is by no means a rule in
        local polo games. There was no limit to the number of players and no
        time limit. Whichever team scored nine goals first, was the winner. The
        present game with a team of six players in a side, lasts one hour with a
        ten minute break.

    </p>
    <p>Gilgit, Chitral and Skardu have always played the game of polo closest to
        its original form. In the past, local Rajas, Mirs and Mehtars were the
        patrons of the game. At times, more than 50% of the annual budget of
        their principalities would be spent on supporting thegame.

    </p>

    <p><b>Shandur Polo Tournament:</b></p>
    <p>The first time a polo tournament took place at the Shandur Pass, was in
        1936. A British Political Agent, Major Cobb, who was fond of playing
        polo under a full moon, had the polo ground near Shandur, named. 'Moony
        Polo Ground'.
    </p>
    <p><img src="/img/activities/festivals_polo_02t.jpg" class="thumb" align="left">The most
        exciting polo tournament of the entire Northern Pakistan, is played on
        top of Shandur Pass, around 3,700 meters above sea level. It is a place
        unique and exotic in itself, surrounded by some of the most spectacular
        mountain scenery in the world. The event marks the annual rivalry
        between the polo teams of Gilgit and Chitral. The Polo tournament has
        some added attractions for the visitors; trout fishing at the nearby
        streams and lakes and a festival of folk dances and music of the
        Northern Pakistan.

    </p>
    <p>The highlight of the festival is the final match between the Gilgit and
        Chitral teams. The final provides a most colorful spectacle. Supporters
        of both sides travel long distances from the remote parts of Chitral and
        Gilgit, to watch the thrilling game. The event, as such, offers a
        fascinating insight into the lifestyle of the people of this region.
        Their culture and indigenous customs are a delight to behold for the
        visitors.

    </p>

    <p><b>Festival Information</b></p>
    <p>It is advisable to be there one day in advance to enjoy all the offerings
        of the festival. Activities at Shandur includes dance and music
        performances by different groups, trout fishing, mountaineering,
        trekking, hiking and of course, horse riding. Crystal clear lakes, snow
        covered mountains, alpine flowers and vast stretches of green grass, are
        added attractions.

    </p>

    <p><b>Facilities</b></p>
    <p>A tourist tent village with restaurant facilities is sprung up during the
        tournament. Merchants from Peshawar, Chitral and Gilgit set up souvenir
        and folk craft shops. The tournament offers visitors and opportunity to
        mix with the locals.

    </p>

    <p><b>How to reach?</b></p>
    <p><img src="/img/activities/festivals_polo_03t.jpg" class="thumb" align="left">The Shandur
        Pass lies almost midway between Chitral and Gilgit on a jeep road,
        travelling on which, is adventurous, to say the least. The distance from
        either side is 170 kms. One can get to Chitral or Gilgit by Pakistan
        International's daily F27 Fokker planes form Peshawar and Islamabad.
        There are daily two flights from both destinations, subject to good
        weather. Visitors planning to go by air must expect adjustment of
        minimum 02 days in their itineraries just in case the weather does not
        permit flights to operate.

    </p>
    <p>Access to Chitral by road is either from Peshawar or from Islamabad. Both
        these routes join up in lower Swat valley near Chakdara, from where you
        proceed via Dir over 3200 meters high Lowari Pass and on to Chitral. It
        will take about 11 hours for a direct ride, otherwise, a stopover in Dir
        is recommended. From Dir, you shift to 4x4 jeeps for your onward journey
        to Chitral and on to Shandur. From Chitral side, travelers can also
        enjoy overnight stay at Mastuj whereas from Gilgit side, Gupis is a
        suitable place.

    </p>
    <p>Travelling by road from Islamabad, on the Karakoram Highway (the Silk
        Route), visitors can stop at halfway for overnight stay along the mighty
        Indus, at Besham.

    </p>

    <p><b>Accommodation</b></p>
    <p>For visitors to Shandur, Pakistan Tourism Development Corporation (PTDC)
