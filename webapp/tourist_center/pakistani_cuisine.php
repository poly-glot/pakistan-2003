        Ranging
        from Seekh Kabas to Koftas. With various other types in between. Seekh
        Kabas are elongated rolls of mince which are cooked on Skewers on a
        charcoal fire. Shami Kabab is mixture of mince meat and daal (pulses),
        both of which are ground together with spices and made into round flat
        patties, and then fried in oil. Chapli Kababs: A peshawari speciality,
        is only mince with spices made into patties and fried in oil too. Koftas
        are mince balls with various fillings. These are then cooked and served
        in a rich spicy gravy.
    </p>

    <br>

    <p><img src="/img/tourist_center//activities_culinary_02t.jpg" class="thumb" align="left"> Besides this, Beef dishes are also quite popular. The
        most famous being the Qormah and the Nihari. Chicken Tikka (whole parts
        of chicken barbecued), Chicken Karahi, Chicken Musallam and Chicken
        Shashlik, are all renowned Chicken specialties.
        Kattakat is a famous delicacy, made from sheep's brains, kidneys and liver. A
        thick soup made by boiling and stewing sheep or goat forelegs, is
        absolutely appetising.
    </p>

    <p>Dips are very commonly used in Pakistan with every meal.
        The most popular types are Raita and Chutneys. Raita is a soothing dip
        made of yogurt, whereas chutney is hotter and spicer and is preferred by
        the more daring.
    </p>

    <p>The most popular dessert is the Ice-Cream and Kulfi.
