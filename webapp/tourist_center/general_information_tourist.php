    <b><font color="#000000" face="Arial Black" size="5">GENERAL INFORMATION FOR TOURISTS</font></b>
    <br>


    <p align="RIGHT">"As often as possible, do what others are not doing: Go
        off-season instead of on, go in bad weather instead of good, walk
        when others ride, laugh when others cry...."<br>Ed Buryn, 1971</p>

    <font size="3" face="Arial" color="#000000"><b>Car Hires / Taxi Service</b></font>
    <p>There are car hire facilities at all International
        Airports. Agencies also function in major hotels. In the streets you can
        get yellow cabs or taxis. Metered taxis of different capacities are
        available everywhere in big cities. In case of difficulty, get the
        assistance of Police officials on duty. On some occasions you may also
        need to exercise some bargaining as well. Try asking a few taxi drivers
        located at different places to get a clear idea of the charges.

    </p>

    <font size="3" face="Arial" color="#000000"><b>Car Rental</b></font>
    <p>Apart from a valid driving licence for self-driven rented
        cars, firms insist on a minimum age of 21 and may refuse to rent a car
        to any one over 60. Pakistan observes, "Right Hand Drive".
    </p>
    <p>Distances on the roads are indicated in Roman numerical
        and also mentioned in Urdu. Sometimes there may be confusion between
        kilometers and miles. Within the city, especially on busy roads, it is
        better to drive within 50 kilometers (31 miles) per hour.

    </p>

    <font size="3" face="Arial" color="#000000"><b>Railways</b></font>
    <p>The railroad network is extensive, but when travelling,
        go first class or in an air-conditioned coach for comfort. Ideally, rent
        a "Tourist Car" which can be attached to any train. Fares are cheap by
        Western Standards and bookings should be done in advance both for seats
        and sleepers. Pakistan Railways also offer special concessions to
        tourists and well maintained Rest Houses are also offered as and when
        available.

    </p>

    <font size="3" face="Arial" color="#000000"><b>Communications</b></font>
    <p>Post &amp; Telegraph Services: Countrywide postal,
        telegraphic and telephone facilities are available. Fax and telex
        services are also available. There are numerous Cyber Clubs throughout
        major cities of Pakistan which can provide you with economical Internet
        facility as well.
    </p>

    <p>Special services offered by Pakistan Post Office include:</p>
    <ul>
        <li>Local Express Service (LES);
        </li>
        <li>Urgent Mail Service (UMS);
        </li>
        <li>Air Express &amp; Int'l Speed Post (ISP);
        </li>
        <li>Surface Air Lifted Mail (SAL).
        </li>
    </ul>

    <p>Telephone &amp; Exchanges</p>
    <p>Telephone facility is available in almost all the cities.
        More than 100 cities are linked with nation wide direct dialing system.
        For codes consult any telephone directory. Cellular mobile telephones
        are also available in big cities. They can also be hired on daily basis.
        Pay card phone booths are also installed at important places in big
        cities.

    </p>

    <font size="3" face="Arial" color="#000000"><b>Courier Services</b></font>
    <p>Courier services for sending your documents and letters
        are available in private sector as well. These services are more
        expensive and the delivery is taken from the sender's address to the
        addressee. These service are also available for international points.

    </p>

    <font size="3" face="Arial" color="#000000"><b>Accommodation</b></font>
    <p>Pakistan's main cities - the Federal Capital, Islamabad
        and its twin city Rawalpindi, as well as the Provincial capitals, except
        Quetta i.e. Lahore, Karachi, and Peshawar have international standard 5
        - Star accommodation for tourists. In Quetta, the Provincial capital of
        Balochistan, a 4-Star Serena Hotel is located. Serena Hotel is also
        located in Faisalabad, Gilgit and Hunza. In all these touristically
        important cities, good international standard accommodation is
        available. Main cities have hotels suitable for every pocket.
    </p>
    <p>Tourists in search of cheaper accommodation should try
        the local Youth Hostels or YMCA's hostels which offer clean
        accommodation and wholesome atmosphere at cheaper fare then the 3 or
        4-Star hotels.
    </p>
    <p>The Rest Houses owned by the various Government
        Departments all over the country are also available for tourists after
        advance bookings. This is, however, a cumbersome process as one has to
        reach the right official who can only rent out the Rest House to a
        tourist if it is not required by a government official during that
        period. In the Northern Areas and Hill Stations, however, Rest Houses
        have been built by local authorities, especially for travellers and
        tourists. The accommodation at these places is good and at places
        watchmen-cum-chefs are available which reduces the expenses of board
        considerable.
    </p>
    <p>PTDC operates Rest Houses or Motels in a number of
        beautiful places where tourists go. These include Ziarat near Quetta,
        Taxila. Moenjodaro and various hill towns in the Northern Areas.

    </p>

    <font size="3" face="Arial" color="#000000"><b>Banks</b></font>
    <p>The State Bank of Pakistan is the Central Bank. There are
        domestic commercial banks - Allied Bank, Habib Bank, National Bank of
        Pakistan, United Bank, Cooperative Banks, 8 development finance
        organisations and 15 principal foreign banks including ANZ Grindlays,
        Abn Amro, Emirates Bank International, American Express and City Bank.

    </p>

    <font size="3" face="Arial" color="#000000"><b>Banking Timings</b></font>
    <p>Office Hours: 9 am to 5 pm with one hour break from
        Monday to Thursday and with two hours break on Friday. Saturday is half
        working day from 9 am to 1:30 pm without break. Sunday is off.
    </p>
    <p>Counter Services : 9 am to 1:00 pm from Monday to
        Thursday and 9 am to 12:30 on Friday and Saturday, with no break.
    </p>
    <p>Special timings may be observed during the month of
        Ramazan, and you should make sure of these timings if you happen to be
        in the country in this holy month.

    </p>

    <font size="3" face="Arial" color="#000000"><b>Credit Cards</b></font>
    <p>Major credit cards such as American Express, Visa, Master
        Card, and others besides Rupee credit cards launched by National Bank of
        Pakistan &amp; Habib Bank etc. can be used for your shopping and other
        transactions.
    </p>

    <font size="3" face="Arial" color="#000000"><b>Laundry &amp; Hair Dressing</b></font>
    <p>First class hotels offer 24-hour laundering and dry
        cleaning services as well as hair dressing facilities. Many Hair
        Dressing Establishments and Beauty Parlours are available in all cities.
        These are generally closed on Mondays.

    </p>

    <font size="3" face="Arial" color="#000000"><b>Medical Facilities</b></font>
    <p>All the cities have modern hospitals and Medical Centers
        staffed by western-trained specialists. There are private clinics as
        well. The European standard medical facilities are pretty cheap. In the
        rural areas, however, you should be careful to carry your own drugs,
        which are easily available in all towns. Foreigners, especially from the
        developed countries are advised to be careful of their drinking water.
        Outside the 4 or 5 Star Hotels, the water may not have been boiled and
        they should make inquiries about it and ensure the standard of drinking
        water, which goes well with their health. They can however, get
        disposable mineral water bottles at economical rates. Tourists are also
        advised to vaccine themselves from their home country before coming to
        Pakistan for diseases like Hepatitis B.

    </p>

    <font size="3" face="Arial" color="#000000"><b>Liquor</b></font>
    <p>Liquor is completely banned for Muslims in Pakistan.
        Foreigners and non-Muslims can have it on permits or in hotels wherever
        available. It cannot be imported in baggage. Import of liquor is banned
        except for the mountaineering, trekking and scientific expeditions.
    </p>
    <p>The process for getting a permit is to get a certificate
        from a Tourist Information Center and then call at the Excise and
        Taxation Office where a nominal fee is charged. In a first class hotel
        you can buy liquor and enjoy it in your rooms. Beer and some other
        drinks are produced locally. These are less expensive than the imported
        ones. The standard however, is just reasonable.
    </p>

    <font size="3" face="Arial" color="#000000"><b>Tipping</b></font>
    <p>Service charges are added to the bill in some restaurants
        and hotels. When tipping for food and other services, use your
        discretion.
    </p>
