    <p>General Cautions</p>

    <p>Recent medical and dental exams should ensure that the
        traveler is in good health. Carry appropriate health and accident
        insurance documents and copies of any important medical records. Bring
        an adequate supply of all prescription and other medications as well as
        any necessary personal hygiene items, including a spare pair of
        eyeglasses or contact lenses if necessary.
    </p>

    <p>Drink only bottled beverages (including water) or
        beverages made with boiled water. Do not use ice cubes or eat raw
        seafood, rare meat or dairy products. Eat well-cooked foods while they
        are still hot and fruits that can be peeled without contamination. Avoid
        roadside stands and street vendors.
    </p>

    <p>Swim only in well-maintained, chlorinated pools or ocean
        water known to be free from pollution. Wear clothing which reduces
        exposed skin and apply repellents containing DEET to remaining areas.
        Sleep in well-screened accommodations. Carry anti-diarrheal medication
        for precautions. Reduce problems related to sun exposure by using
        sunglasses, wide-brimmed hats, sunscreen lotions and lip protection.
    </p>

    <p>Immunizations</p>
    <p>These recommendations are not absolute and should not be
        construed to apply to all travelers. A final decision regarding
        immunizations will be based on the traveler's medical history, proposed
        itinerary, duration of stay and purpose for traveling.
    </p>
    <p>Hepatitis A: Consider active immunization with hepatitis
        A vaccine or passive immunization with immune globulin (IG) for all
        susceptible travelers. Especially consider choosing active immunization
        for persons planning to reside for a long period or for persons who take
        frequent short-term trips to risk areas. The importance of protection
        against Hepatitis A, increases as length of stay increases. It is
        particularly important for persons who will be living in or visiting
        rural areas, eating or drinking in settings of poor or uncertain
        sanitation, or who will have close contact with local persons
        (especially young children) in settings with poor sanitary conditions.
    </p>
    <p>Hepatitis B: Vaccination is advised for health care
        workers, persons anticipating direct contact with blood, and persons
        planning extended stays of 6 months or greater (especially those who
        anticipate using local health care facilities, staying in rural areas,
        or having intimate contact with the local population).
    </p>
    <p>Japanese Encephalitis: Consider vaccination if staying a
        month or more in the central river deltas or areas near Karachi from
        June to January when transmission is presumed to occur, especially if
        travel includes rural areas. Also consider if staying less than 30 days
        during that period and at high risk (in case of epidemic outbreak or
        extensive outdoor exposure in rural areas).
    </p>
    <p>Polio: A one-time booster dose is recommended for
        travelers who have previously completed a standard course of polio
        immunization. Pregnancy is a relative contraindication to vaccination;
        however, if protection is needed, either IPV or OPV may be used,
        depending on preference and time available.
    </p>
    <p>Rabies: Pre-exposure vaccination should be considered for
        persons staying longer than 30 days who are expected to be at risk to
        bites from domestic and/or wild animals (particularly dogs), or for
        persons engaged in high risk activities such as spelunking or animal
        handling. Need for vaccination is more important if potential exposure
        is in rural areas and if adequate post-exposure care is not readily
        available.
    </p>
    <p>Typhoid: Vaccination should be considered for persons
        staying longer than 3 weeks, adventurous eaters, and those who will
        venture off the usual tourist routes into small cities, villages and
        rural areas. Importance of vaccination increases as access to reasonable
        medical care becomes limited. Contraindications depend on vaccine type.

    </p>
    <p>Note: All routine vaccines should be kept up-to-date as a
        matter of good health practice unrelated to travel.</p>
