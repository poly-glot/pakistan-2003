    an unspoiled paradise! This is why it can be a deeply satisfying experience
    to spend a few days in Kaghan. Kaghan is at its best during summer (months
    ranging from May to September). In May the temperature is: maximum 11 C
    and minimum 3 C. From the middle of July up to the end of September the
    road beyond Naran is open right upto Babusar Pass. Movement is restricted
    during the monsoon and winter seasons. The Kaghan area can reached by road
    via the towns of Balakot, Abbottabad and Mansehra. In Balakot, one may
    find buses and other transports to reach Kaghan or Naran.</p>

    <p>The road from Balakot ascends along the Kunhar River through lovely forests
        and the villages of Paras, Shinu, Jared and Mahandri. The valley is somewhat
        narrow along this stretch and the views are limited but as you ascend,
        the sorrounding peaks come into view. One spot that is quite famous for
        its spectacular view and scenary is 'Shogran'. This village, sorrounded
        by peaks and forests, is east of the main Kunhar River. It hosts the famous
        Siri-Paya mountain with breathtaking views at its top.</p>

    <p><img src="/img/destinations/mountains_valleys/kaghan_valley/kaghan_03t.jpg" class="thumb" align="left">Fishing is the chief sport in Kaghan. Brown
        Trout and Mahasheer are stocked in pure silvery waters between Kaghan and
        Naran. The Kunhar river trout is considered to be the best throughout the
        sub-continent. Fishing licenses are issued by the 'Fisheries Department
        at Naran' or by the 'Trout Hatchery' at Shinu.</p>

    <p>Most visitors to Naran pay a visit to Saiful Maluk Lake (10,500 feet)
        6 miles east of town. If the road is open you can arrange transportation
        by jeep. If the road is closed, it is an easy, gradual three-hour walk,
        and the lake is a lovely spot for a picnic.</p>

    <p><img src="/img/destinations/mountains_valleys/kaghan_valley/kaghan_04t.jpg" class="thumb" align="left">If you are walking directly up-valley from Naran to Babusar "Top" the
        loveliest spots to camp on this trail is at "Lulusar Lake". Located just
        before the final grade to Babusar Top and sorrounded by tall peaks, Lulusar
        is just one many high elevation lakes that sit along the crest of the ridge.
    </p>

    <p>One of the most interesting features of the Kaghan area is the Gujar (herder)
        families you'll see along the way bringing their animals upto the summer
        pastures. The Kaghan valley is one of their most popular destinations in
        Pakistan, and you'll find them camped along the road in their tents or
        moving up the valley with their goats, sheep and pack animals. For those
        with time and interest, this peaceful, lovely valley offers an interesting
        and enjoyable destination.</p>
