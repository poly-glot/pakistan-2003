    <p><img src="/img/destinations/mountains_valleys/chitral_valley/chitral_01t.jpg" class="thumb" align="left">The Chitral Valley located at an elevation of
        1,128 metres is one of the most popular spots amongst mountaineers, anglers,
        hunters, hikers, naturalists and anthropologists. The 7,705 metres Tirich
        Mir, the highest peak of the Hindukush mountains, dominates this 322 kms
        long exotic valley.</p>

    <p>The Chitral district has Afghanistan at its north, south and west. A narrow
        strip of Afghan territory, Wakhan, separates it from Tajikistan. The tourist
        season in Chitral is from June to September. The maximum temperature in
        June is 35 C and the minimum is 19 C. In September, the maximum goes upto
        24 C and the minimum temperature falls down to 8 C. Which makes your stay
        pleasant and comfortable.</p>

    <p><img src="/img/destinations/mountains_valleys/chitral_valley/chitral_02t.jpg" class="thumb" align="left">The Muslim tradition of offering hospitality
        to the 'muzzafer', the traveller, is observed throughout Pakistan's Northern
        Areas, but in few places is it offered so genuinely as in Chitral. If someone
        should invite you to stay in his house, it will be an unforgettable experience.
        Most village homes in Chitral have a large family room called "Baipash".
        In the middle of the Baipash is a fire place with home spun rugs arranged
        near it upon which the family and neighbours sit. Some well-to-do people
        have a separate room especially for visitors called an "Angotee".</p>

    <p>Chitral has a strong musical tradition. The "Chitrali Sitar" a graceful
        string instrument, is heard daily at small workingmen's teahouses in Chitral
        Bazaar. The 'Chenai' and 'Dol' (Oboe and Drums) frenetically accompany
        all polo matches.</p>

    <p><img src="/img/destinations/mountains_valleys/chitral_valley/chitral_03t.jpg" class="thumb" align="left">One of the major attractions of Chitral, is
        the famous Kalash valley - the home of the Kafir-Kalash or "Wearers of
        the Black Robes", a primitive pagan tribe. Their ancestry is enveloped
        in mystery and is the subject of controversy. A legend says that five soldiers
        of the legions of Alexander of Macedonia settled in Chitral and are the
        progenitors of the Kafir-Kalash.</p>

    <p>Over 3,,000-strong Kafir-Kalash live in the valley of Birir, Bumburet
        and Rambur, south of Chitral. Bumburet, the largest and the most picturesque
        valley of the Kafir-Kalash, is 40 kms. from Chitral and is connected by
        a jeepable road. While Birir and Rambur are located at a distance of 34
        and 32 kms respectively from Chitral.</p>

    <p><img src="/img/destinations/mountains_valleys/chitral_valley/chitral_04t.jpg" class="thumb" align="left">The Kalash women wear five large braids of and
        the 'Cheo', a black woolen homespun dress, red-beaded necklaces by the
        dozen, and an exceptional head piece (shaped differently in each valley)
        covered in cowrie shells, beads and trinkets that flow down their back.</p>

    <p>The Kalash are a friendly and cheerful bunch, who love music and dancing,
        particularly on their religious festivals like Joshi Chilimjusht (From
        14th - 21st December).</p>

    <p>Polo is the most popular sport of Chitral. It holds similar importance
        amongst the Chitral &amp; Gilgit populations. Polo matches are great attractions
        and festive occasions for all tourists. Even if you do'nt have a knack
        for this game you'll surely love the atmosphere it creates. A regular Polo
        tournament (Shandur Polo Tournament) is held every year (First week of
        July) at Shandur Pass. It is indeed a wonderful tourist attraction!</p>
