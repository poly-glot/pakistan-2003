    <b><font color="#000000" face="Arial Black" size="5">K-2</font></b>
    <br>

    <p>K2 is a rocky mountain climbing upto 6,000 m, beyond which, it becomes
    an ocean of snow. K2 peak is situated on the Pak-China border in the mighty
    Karakoram Region. The traditional route to its base camp goes from Skardu,
    which is linked with Islamabad by a fairly good road, as well as, by air.
    From Skardu the route goes via Shigar-Dassu-Askole upto Concordia over
    the Baltoro glacier. The exact height of the peak is 8,611 m/28,251 ft.
    However, some American magazines show its height as 8,760m/28,740ft which
    does not seem to be correct.</p>
<p>It was in 1856, when the British were enforcing their control over India,
    provoking the 1857-War-of-lndependence, that a young Lieutenant of the
    Royal Engineers, T.G. Montgomerie, was quietly busy in surveying the mountains
    of Kashmir. During this survey he saw, in the far distance, a tall and
    conspicuous mountain in the direction of the Karakorams and immediately
    named it K1 ('K' stands for Karakorams). Later on, it turned out to be
    the beautiful mountain of Hushe valley in Khaplu area of Baltistan, called
    Masherbrum by locals. He also saw another tall and dominating summit behind
    K1 and named it K2, which turned out to be "Chogori". The name K2, however,
    still stands.</p>
<p>Lieutenant Montgomerie was a good triangulator. It was he who planned
    and organised the survey of Kashmir. He was also an unofficial political
    adviser to Gulab Singh, the then Maharaja of Kashmir. After Gulab Singh's
    death in 1857, Montgomerie continued his survey-work as he carried the
    same influence with Maharaja Ranjit Singh, the then successor of Gulab
    Singh. Montgomerie trained many locals in the art of survey. His students
    did good reconnaissance work in remote areas forbidden to foreigners because
    of local suspicions. A famous but unfortunate student of his was Muhammad
    Hameed.</p>
<p><img src="/img/destinations/mountain_peaks/k_2/k2_02t.jpg" class="thumb"
    align="left">In 1860, Captain Henry Haversham Godwin-Austen, of the Survey
    of India, went to Baltistan area and surveyed the famous Shigar and Saltoro
    valleys. This greatly contributed to the knowledge of the area. He was
    an officer in the 24th Foot Battalion, later the South Wales Borderers,
    and had also served in the Second Anglo- Burmese War in 1852. Earlier,
    he had joined Montgomerie at a survey station in Kashmir in 1857. He also
    surveyed the Kajnag range in southern Kashmir and was the first to put
    Gulmarg on the map. In 1858-59, he surveyed eastern Kashmir including Jammu.
    In 1861, he started from Skardu and entered Braldu valley from Skoro-La
    (5,043m). He then climbed and surveyed Chogo-lungma, Kero Lungma, Biafo
    and Panmah glaciers. It was from Kero Lungma that Godwin-Austen climbed
    the Nushik pass (4,990m/1 6,371 ft) and is stated to have entered the 53-km-long
    Hispar glacier. He was perhaps the first European to reach there. He, however,
    did not survey it. He was considered to be one of the greatest mountaineers
    with great power of endurance and bravery. It is a myth that the K2 peak,
    which is erroneously called Godwin-Austen peak, was discovered by him.
    It is, however, a fact that he explored the gateway to K2 i.e. Baltoro
    glacier, alongwith famous glaciers including Godwin-Austen glacier. This
    was indeed his outstanding contribution to the geography of the area.</p>
<p>Another famous explorer of the area was Francis Younghusband (later knighted),
    a noted soldier and thrill-seeker. Showing his courage and tenacity in
    1887, he crossed Gobi desert from Peking and entered India by crossing
    Mustagh pass. It was during this journey that he saw K2. In this way he
    was the first European to cross Mustagh pass. He was also the first European
    to set eyes on K2 from the northern side. His guide on this inward journey
    was a former resident of Askole village, situated at the start of Baltoro
    glacier, who had been living on the other side of the mountain for a very
    long time. When he entered the village of Askole with his guide, Younghusband
    was extended due courtesies. "His guide was, however, looked down upon
    because he had shown a foreigner the possible route of invasion." Subsequently
    in 1903-4, Sir Francis Younghusband became the head of the famous mission
    to Tibet.</p>
<p><img src="/img/destinations/mountain_peaks/k_2/k2_03t.jpg" class="thumb"
    align="left">It was probably for the first time in 1902 that an organised
    expedition of Oscar J.L. Eckenstein travelled to K-2 from Baltoro glacier.
    The expedition was without any guide. Its aim was to explore approaches
    to the mountain and possibly have a try on the peak. It was, however, harsh
    weather which prevented it from attempting the peak. The party, however
    collected useful information about the upper Godwin-Austen glacier which
    was used as a stepping stone by expeditions in later years. Two members
    of the expedition - one a Swiss by the name of Dr. Jules Jacot Guillarmot
    and the other an Austrian by the name of Dr. V. Wessley - succeeded in
    reaching 6,523m (21,400ft) on the north-eastern ridge of K-2. The party
    also ascended Skyang La (6,1 50m) to ascertain climbing possibilities of
    Skyang Kangri peak (7,544m). Eckenstein was the first mountaineer who applied
    the principles of engineering to mountaineering and its equipment.</p>
<p>In 1909, a big Italian expedition under the leadership of resolute Luigi
    Amadeo Giuseppe (Duke of Abruzzi) the grandson of King Victor Emmanuel
    II of Italy, journeyed the peak. Its members produced a very good account
    of the expedition with photographs and accurate maps of Baltoro area. The
    Duke, however, rejected the southern and western ridges of the mountain
    for a climb. His party attempted the peak from the south-east ridge-which
    later came to be known as Abruzzi ridge - but could not proceed beyond
    5,560m/1 8,242ft because of porter problems. The party, however, carried
    out a thorough reconnaissance of K2 from south to north-east. Vittono Sella,
    a photographer and a climber, accompanied the Duke on this expedition.
    Sella pass, near Godwin-Austen glacier, is named after him.</p>
<p>Two famous British mountaineers, Harold William Tilman and Eric Earle
    Shipton, explored and surveyed the north face of K2 and its subsidiary
    glaciers in 1937. Actually they were on a survey mission to Shaksgam valley
    when they also visited the Trango and Sarpo Laggo glaciers. They also explored
    and surveyed the famous Skamri glacier. Tilman was a famous explorer, mountaineer,
    sailor and writer. He also distinguished himself as a planter in Kenya.</p>
<p>Shipton, on the other hand, was one of the significant explorers of the
    present century. He was Tilman's companion on most of the expeditions.
    Shipton was also Consul-General of India in Kashgar in 1940-42 and then
    in 1946-48.</p>
<p>In 1938, the American Alpine Club sponsored a reconnaissance party for
    a visit to the K2 area. The party reached a height of 7,925m/26,000ft after
    setting up eight camps. When compared with the heights climbed by previous
    expeditions, this seems to be a considerable advancement. Famous American
    mountaineers like Dr. Charles Houston and Robert Bates were in this party.
    Six sherpas from Nepal were also on this expedition as porters etc... After
    a proper reconnaissance of the routes leading to K-2, the party rejected
    the north-west and north-east routes. Instead, it selected the south-east
    ridge (Abruzzi ridge). It was the shortage of food supplies that forced
    Houston and Petzoldt to return to lower altitudes. In the opinion of the
    party it was through this ridge that K2 peak could be climbed, which eventually
    proved correct.</p>
<p><img src="/img/destinations/mountain_peaks/k_2/k2_04t.jpg" class="thumb"
    align="left">The next year saw another American expedition on K-2. It was
    led by Fritz Hermann Ernst Wiessner, a German-American chemist and mountaineer.
    The expedition, alongwith nine sherpas, made very good progress on the
    already-identified south-east ridge. Two members and five sherpas set up
    camp VIII at about 7,711 m/25,300ft and left one member by the name of
    Dudley Wolfe in this camp as he had fallen sick. Wiessner, alongwith one
    sherpa, went up to approximately 8,382m/27,500ft. On their way back they
    found that Wolfe was short of food. They, therefore, hurriedly brought
    him down to camp VII and made him stay there. They then descended in search
    of food and aid but found all camps abandoned until they reached camp II.
    Immediately three sherpas were sent to rescue Wolfe. They, however, did
    not return. In this way, Wolfe and the sherpas died on the K2. What a tragic
    but heroic death.</p>
<p>Another American attempt on the K2 was made in 1953. The expedition leader
    was Dr. Charles Houston, who had also led the 1938 American expedition
    on this peak. Dr. Houston, medical professor, is noted for his contribution
    to research on the effects of high altitude on human body and diseases
    originating from such effects. One Pakistani, late Colonel M. Ataullah,
    Vice President, Karakoram Club of Pakistan, accompanied the party. This
    time the party took porters from Hunza instead of sherpas from Nepal. As
    against the previous expeditions, which entered Baltistan from Srinagar
    (in the Indian occupied Kashmir) through a very long route, the party flew
    into Skardu and then adopted the traditional route to K2 over Baltoro glacier.
    It was at camp VIII, at about 7,772m/25,500ft that the party was hit by
    a blizzard which lasted many days. On the 7th August one member, Arthur
    Gilkey, developed thrombophlebitis. In view of his serious condition it
    was decided to start descent in spite of bad weather. At the end of the
    day, the party was involved in a "fall on a steep slope as a result of
    a slip and tangling of ropes". Luckily nobody was seriously injured. Subsequently
    all members assembled at the nearby camp VII. Gilkey was secured on the
    snow slope with two ice axes until a party could be mustered to bring him
    across the slope to the camp. However, when three members of the party
    returned to Gilkey, they found that he had been swept away by an avalanche.
    It took rest of the party five hard days to reach to the base camp. Upon
    reaching there, the party immediately started for Skardu because of very
    bad frost-bitten feet of one of the members, George Bell. In this way,
    in spite of very best efforts, the Americans could not climb K2 from the
    south-east ridge.</p>
<p><img src="/img/destinations/mountain_peaks/k_2/k2_05t.jpg" class="thumb"
    align="left">In 1954, an Italian expedition came to Pakistan to try its
    luck on K2. It consisted of twelve climbers and four scientists and was
    led by veteran mountaineer, Professor Ardito Desio, who had come to these
    mountains with Italian expeditions before the World War II. Colonel M.
    Ataullah and Arshad Munir accompanied the expedition from Karakoram Club
    of Pakistan. Captain (later Lt. General) G.S. Butt was the liaison officer.
    Poor weather hindered progress of the party for a pretty long time. As
    soon as the weather cleared, the party made very good progress and set
    up camp II. It was at this camp that one of its members, Mario Puchoz,
    a 36-year old guide, died of pneumonia on the 21st June. It Is believed
    that he had contracted high altitude pulmonary oedema (water on the lungs)
    which was not well known at the time and does not respond to antibiotics.</p>
<p>The party established six more camps on the south-east ridge. Camp IX
    was a bivouac. On the 31st July, Lino Lacedelli and Achille Compagnoni
    started from the bivouac. They exhausted their oxygen supply 500 feet short
    of summit but could not resist the temptation to be the first to climb
    the mighty K2 peak. They, therefore, continued their assault and reached
    the summit at six in the evening. After staying for a while they started
    descending and reached camp VIII round about eleven at night.</p>
<p>In this way the saga of K2 ended. The story of the climb has been narrated
    by Professor Desio in his book. "Victory over K-2" in a detailed manner.
    But alas this book is available only in a few foreign libraries.</p>
<p>The peak has now been climbed from almost all ridges. Demand to climb
    it from different, newly identified and difficult routes is on the increase.
    This is because the peak has yet to be climbed for as many times as its
    elder sister, the Everest.</p>
