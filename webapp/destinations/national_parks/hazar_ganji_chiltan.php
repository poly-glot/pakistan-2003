<p>Hazar Ganji Chiltan National Park has given protection to Markhor, which
    is Pakistan's National Animal. Being on the endangered animal list it is
    kept in The HazarGanji Chiltan National Park. Eversince, protection has
    been given to Markhor it's number has multiplied.</p>
<p>Other animals kept in the park are straight horned Markhors, "Gad" (wild
    sheep) and leopards which occasionally migrate to the park from other areas,
    wolves, striped hyena, wild cats and porcupines.</p>
<p>Many birds like partridge, warblers, shikras, blue rock pigeon, rock nuthatch,
    red gilled choughs, golden eagle, sparrow, hawks, falcons and bearded vultures
    are either found here or visit the park in different seasons.</p>
<p>Reptiles like monitor and other wild lizards, geckos, Afghan tortoise,
    and python, cobra, horned viper and levantine may also be seen in the park.</p>
<p>Amongst the flora of the park are some 225 species of plants. Prominent
    are the pistachio's, Juniper, wild olives, wild ash and wild almonds. Many
    shrub's like wild fig, barbery, wild cherry, makhi etc; provide food and
    shelter to the foraging animals, birds and other life form. Many medicinal
    herbs are also found there. There is a splash of color during the spring
    season when most of the plants are in full bloom. Nature lovers, students,
    scientists and researchers are all welcome to visit this splenderous beauty.
    Permits for visiting the park can be obtained from the Divisional Forest
    Officer, Spiny Road, and Quetta.</p>
<p>Rest house facilities are also available for an overnight stay. Park Rangers
    help the visitors in observing these animals. Access trails have also been
    developed to facilitate the visitors.</p>
<p>Wildlife:</p>
<p>Mammals found in this park include; Chiltan wildgoat or Markhor, straight-horned
    Markhor, wild sheep (Gad), Indian wolf, Stripped hyaena, Caracal (unconfirmed),
    Jackal, Indian fox, Porcupine, Desert hare and the Asiatic Leopard that
    ocassionally migrate from other areas.</p>
<p>Birds may include; Houbara bustard, Griffon vulture, Honey buzzard, Laggar
    falcon, Peregrine falcon, Kestrel, Indian sparrow hawk, Egyptian vulture,
    Larks, Shrikes, Wheatears etc.</p>
<p>Amongst the Reptiles one may find; Monitor lizard, Russells viper, Indian
    cobra, Saw scaled viper and Spiny tailed lizard. With so much to see visitors
    can have a busy time in this park.</p>
