    <p>A little short of their destination, Nanak and Bala came across group
    of sadhus, naked and hungry. The former at once decided to feed them, and
    despite Bala's advice not to do so, bought provisions from Chuharkana and
    served them a hearty meal.</p>

    <p>On his return home empty handed, Guru Nanak's father rebuked him for squandering
    his precious cash, but he calmly answered, "you had wanted me to make 'Khara
    Saudha' (Lit. real bargain); I could not think of a better and more genuine
    one than the one I have made." The Gurdwara established during the Sikh
    Rule on the spot where the sadhus were entertained came to be called Sacah
    Soda. Now called Gurdwara Sacha Sauda (Lit. true bargain), it stands between
    the mandi (market) and the town of Chuharkana. Its domed sanctum in the
    middle of a square hall stands in a fort-like enclosure. Khalsa Divan Bar,
    under the leadership of Jathedar Kartar Singh Jhabbar, liberated the Gurdwara
    from the centuries-old control of Udasi mahants on December 30, 1920.</p>
