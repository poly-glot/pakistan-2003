        Chaukundi means four cornered and thus the site became famously known as chaukundi.
        The distinguishing feature of these graves is the superb carving and engraving on the slabs with
        various designs of jewelry; floral patterns, horses and even their
        riders. The tomb slab of a woman's grave is embellished with designs of
        jewelry, necklace, earrings and rings resembling those still worn today.
        The men's graves bears stylised stone turban on top, carvings of weapons
        of war or animal heads, horses and riders. This design may have
        originated in the Rajput custom of temporarily burrying a fallen soldier
        in the battlefield and marking his grave with his upright sword crowned
        with his turban.

