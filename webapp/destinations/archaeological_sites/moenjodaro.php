    <p>Moenjodaro had mud-brick and baked-brick buildings. Covered drainage
        system in addition to this, soakpits for disposal bins, a large state
        grannary, a spacious pillared hall, a collage of priests, a large and
        imposing building (probably a palace) and a citadel mound which
        incorporates in its margin a system of solid burnt brick tower.

    </p>

    <p>Moenjodaro looks like a planned, organized and master architecture of
        urban settlement. Beneath the citadel, parallel streets, some 30 feet
        wide, stretched away and <img src="/img/destinations/archaeological_sites/moenjodaro/moenjodaro_02t.jpg" class="thumb" align="left"> are crossed by other straight
        streets, which divide the town into a great oblong block, each 400 yards
        in length, and 200 to 300 yards in width. The most imposing remains are
        those of a Great Hall which consisted of an open quadrangle with
        verandahs on four sides, galleries and rooms on the back, a group of
        halls on the north and a large bathing pool. It was probably used for
        religious or ceremonial bathing.
    </p>

    <p>Nearby are the remains of Great Granary, possibly a public treasury where
        taxes were paid. We can assume the sensibility of the artistic mind by
        the discovery of necklaces, pendants of beads, earrings and ankles of
        ivory and mother of pearl, vessels of silver and bronze and stone
        weights and measures which suggests the existence of stringent civic
        regulations.
    </p>

    <p>When the first seal was found in Harappa in 1875 it was thought to be of
        a foreign origin. A humpless bull with an illegible inscription
        comprising six characters, were engraved on dark brown jasper.
    </p>
    <p>
        <img src="/img/destinations/archaeological_sites/moenjodaro/moenjodaro_03t.jpg" class="thumb" align="left"> The significance of this, and the
        multitude of other seals to follow, was felt when it was realized that
        the engraved characters and pictures are not only indigenous to the
        Indus civilization but a thorough understanding of engravings can give a
        comprehensive account of the true nature of the Indus civilization.
        First it was thought that the pictograph and the ideograms were related
        to Brahmi characters or where perhaps the forerunner of Brahmi. It was
        also suggested that the seal was ideophonographic and resembled the
        Hieroglyphic seal.
    </p>

    <p>On the other hand work was being carried on to prove that Dravdian had
        inherited many Indus signs thus proving the Dravdic affinity of the
        Indus seal. Its destruction was by the hands of invading Aryan hordes,
        as some historians believe, or was triggered by an earthquake, or flood
        the remains of which are yet to be established. The Moenjodaro museum,
        close to the site of excavation, houses price-less relics found there,
        including these engraved seals, ornamental utensils, pottery, weapons
        and toys. Some of the precious things have been recently shifted from
        the site museum to the local government treasury.

    </p>

    <p>
        <img src="/img/destinations/archaeological_sites/moenjodaro/moenjodaro_04t.jpg" class="thumb" align="left">Interestingly, the bullock carts, boats,
        drinking jars, toys used even today in the adjoining areas, bare strong
        resemblance to those used by the ancient citizens of Moenjodaro. Through
        the discovery of coins and potteries, archeologists believe that trade
        and cultural links existed between Moenjodaro and the contemporary
        civilizations of Mesopotamia and Egypt.
    </p>

    <p>Various objects d'art found at Moenjodaro include burnt clay male and
        female figurines, and models of bird, a steatite bust a noble man or a
        priest-king, wearing a loose robe on which the tretoil pattern is
        engraved and a small dancing girl of bronze with slim figures and flat
        negroid features. Steatite seals bearing lifelike representation of
        animals and mythological creatures such as the unicorn best illustrate
        the figural. They bear short inscription in a remarkable pictographic
        script, which has yet to be deciphered.
    </p>

    <p>The various layers of the excavated site show that upon the debris of the
