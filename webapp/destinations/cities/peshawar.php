        <p>Peshawar owes its founding some 2,000 years ago to the same Kushans.
            In the second century A.D, Kanishka, the greatest of the Kushan
            kings, moved his winter capital here from Pushkalavati, 30
            kilometers (20 miles) to the north. His summer capital was north of
            Kabul at Kapisa, and the Kushans moved freely back and forth through
            the Khyber Pass between the two cities, from which they ruled their
            enormous and prosperous empire for the next 400 years. Sher Shah
            Suri, successor or rather, the usurper of his son's throne, turned
            Peshawar's renaissance into a boom when he ran his Delhi-to-Kabul
            Shahi Road through the Khyber Pass. The Mughals turned Peshawar into
            a 'city of flowers' (one of the meanings derived from its name) by
            planting trees and developing gardens.
        </p>

        <p>In 1818, Ranjit Singh captured Peshawar for his Sikh Empire. He
